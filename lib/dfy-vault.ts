// Function to get targets by niche
export function getTargetsByNiche(nicheId: string | null): DFYTarget[] {
  if (!nicheId) return DFY_VAULT_FULL
  return DFY_VAULT_FULL.filter(target => target.niche === nicheId)
}

// Function to get random targets
export function getRandomTargets(count: number): DFYTarget[] {
  const shuffled = [...DFY_VAULT_FULL].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

// Stats
export function getDFYStats() {
  return {
    totalTargets: DFY_VAULT_FULL.length,
    totalComments: DFY_VAULT_FULL.reduce((sum, t) => sum + t.readyComments.length, 0),
    totalViews: DFY_VAULT_FULL.reduce((sum, t) => sum + t.views, 0),
    totalEngagement: DFY_VAULT_FULL.reduce((sum, t) => sum + t.likes + t.comments + t.shares, 0),
    niches: NICHES.length,
    avgCommentsPerTarget: Math.round(
      DFY_VAULT_FULL.reduce((sum, t) => sum + t.readyComments.length, 0) / DFY_VAULT_FULL.length
    ),
  }
}

